using UnityEngine;

public class Background : MonoBehaviour {

    public GameObject background;
    private GameObject[] backgroundTiles;

    public int tiles = 0;

    private void Start()
    {
       GameObject back = Instantiate(background, transform.position, Quaternion.identity);
        back.transform.rotation = Quaternion.Euler(new Vector3(0f, 0f, 90f));
        backgroundTiles[tiles] = back;
        tiles++;
    }

    public void AddTile()
    {
        GameObject back = Instantiate(background, transform.position, Quaternion.identity);
        back.transform.rotation = Quaternion.Euler(new Vector3(0f, 0f, 90f));
        backgroundTiles[tiles] = back;
        tiles++;
    }
}
