using UnityEngine;
using UnityEngine.UI;

public class StartButton : MonoBehaviour {

    public GameObject sUI;
    public RocketController player;

    private void Awake()
    {
        sUI.SetActive(true);
        player.enabled = false;
    }

    public void StartGame()
    {
        sUI.SetActive(false);
        player.enabled = true;
    }

}
