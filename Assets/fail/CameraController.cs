using UnityEngine;

public class CameraController : MonoBehaviour {

    public void goLeft(float amount)
    {
        //Moves Camera Left when Player Moves Right to keep the camera in it's origional position
        transform.Translate(new Vector2(amount, 0));
    }

    public void goRight(float amount)
    {
        //Moves Camera Right when Player Moves Left to keep the camera in it's origional position
        transform.Translate(new Vector2(amount, 0));
    }

}
